    ys = [entry[metric_key] for entry in history if metric_key in entry]
    if len(xs) == 0:
        print(f"No tracked values found for {stage_name} / {metric_key}.")
        return

    raw = np.asarray(ys, dtype=float)
    smooth = smooth_history(raw, HISTORY_SMOOTHING_WINDOW)

    plt.figure(figsize=(6, 4))
    plt.plot(xs, raw, marker="o", alpha=0.25, linewidth=1.0, label="raw")
    plt.plot(xs, smooth, marker="o", linewidth=2.0, label="smoothed")
    plt.title(title)
    plt.xlabel("Epoch / checkpoint")
    plt.ylabel("Lower is better")
    plt.grid(True, alpha=0.3)
    plt.legend(frameon=False)
    plt.tight_layout()
    plt.show()

