# Block 4: ODE integration and sampling helpers

@torch.no_grad()
def integrate_flow(
    model: nn.Module,
    x0: torch.Tensor,
    num_steps: int,
    solver: str = "euler",
    return_path: bool = False,
):
    if num_steps < 1:
        raise ValueError("num_steps must be >= 1")

    model.eval()
    x = x0.clone()
    if return_path:
        path = [x.clone()]

    if num_steps == 1:
        t0 = torch.zeros(x.shape[0], 1, 1, 1, device=x.device, dtype=x.dtype)
        x = x + model(x, t0)
        if return_path:
            path.append(x.clone())
            return torch.stack(path, dim=0)
        return x

    dt = 1.0 / num_steps
    t_scalar = 0.0
    for _ in range(num_steps):
        if solver == "euler":
            t = torch.full((x.shape[0], 1, 1, 1), t_scalar, device=x.device, dtype=x.dtype)
            v = model(x, t)
            x = x + dt * v
        elif solver == "midpoint":
            t = torch.full((x.shape[0], 1, 1, 1), t_scalar, device=x.device, dtype=x.dtype)
            v1 = model(x, t)
            x_mid = x + 0.5 * dt * v1
            t_mid = torch.full((x.shape[0], 1, 1, 1), t_scalar + 0.5 * dt, device=x.device, dtype=x.dtype)
            v2 = model(x_mid, t_mid)
            x = x + dt * v2
        else:
            raise ValueError("solver must be 'euler' or 'midpoint'")

        t_scalar += dt
        if return_path:
            path.append(x.clone())

    if return_path:
        return torch.stack(path, dim=0)
    return x


@torch.no_grad()
def sample_images(
    model: nn.Module,
    n_samples: int,
    batch_size: int,
    num_steps: int,
    device: torch.device,
    solver: str = "euler",
):
    outs = []
    remaining = n_samples
    while remaining > 0:
        cur = min(batch_size, remaining)
        x0 = torch.randn(cur, 1, 28, 28, device=device)
        x1 = integrate_flow(model, x0, num_steps=num_steps, solver=solver, return_path=False)
        outs.append(x1.cpu())
        remaining -= cur
    return torch.cat(outs, dim=0)


