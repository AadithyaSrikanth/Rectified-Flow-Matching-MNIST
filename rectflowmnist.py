class ConvBlock(nn.Conv2d):
    def __init__(
        self,
        in_channels: int,
        out_channels: int,
        kernel_size: int,
        activation_fn: bool = False,
        stride: int = 1,
        padding: str | int = "same",
        dilation: int = 1,
        groups: int = 1,
        bias: bool = True,
        gn: bool = False,
        gn_groups: int = 8,
    ):
        if padding == "same":
            padding = (kernel_size // 2) * dilation
        super().__init__(
            in_channels,
            out_channels,
            kernel_size,
            stride=stride,
            padding=padding,
            dilation=dilation,
            groups=groups,
            bias=bias,
        )
        self.activation_fn = nn.SiLU() if activation_fn else None
        self.group_norm = nn.GroupNorm(gn_groups, out_channels) if gn else None

    def forward(self, x: torch.Tensor, time_embedding: torch.Tensor | None = None, residual: bool = False):
        if residual:
            if time_embedding is None:
                raise ValueError("time_embedding is required when residual=True")
            x = x + time_embedding
            y = x
            x = super().forward(x)
            y = y + x
        else:
            y = super().forward(x)

        if self.group_norm is not None:
            y = self.group_norm(y)
        if self.activation_fn is not None:
            y = self.activation_fn(y)
        return y


class ImageFlow(nn.Module):
    def __init__(self, image_resolution=(28, 28, 1), hidden_dims=(128,) * 6):
        super().__init__()
        _, _, img_c = image_resolution
        hidden_dims = list(hidden_dims)
        if len(hidden_dims) < 2:
            raise ValueError("Need at least two hidden dimensions.")

        self.in_project = ConvBlock(img_c, hidden_dims[0], kernel_size=7)
        self.time_project = nn.Sequential(
            ConvBlock(1, hidden_dims[0], kernel_size=1, activation_fn=True),
            ConvBlock(hidden_dims[0], hidden_dims[1], kernel_size=1),
        )

        self.convs = nn.ModuleList([ConvBlock(hidden_dims[0], hidden_dims[0], kernel_size=3)])
        for idx in range(1, len(hidden_dims)):
            dilation = 3 ** ((idx - 1) // 2)
            self.convs.append(
                ConvBlock(
                    hidden_dims[idx - 1],
                    hidden_dims[idx],
                    kernel_size=3,
                    dilation=dilation,
                    activation_fn=True,
                    gn=True,
                    gn_groups=8,
                )
            )

        self.out_project = ConvBlock(hidden_dims[-1], img_c, kernel_size=3)

    @staticmethod
    def interpolate(x0: torch.Tensor, x1: torch.Tensor, t: torch.Tensor) -> torch.Tensor:
        return (1.0 - t) * x0 + t * x1

    @staticmethod
    def target_velocity(x0: torch.Tensor, x1: torch.Tensor) -> torch.Tensor:
        return x1 - x0

    def forward(self, x: torch.Tensor, t: torch.Tensor) -> torch.Tensor:
        if t.ndim == 1:
            t = t[:, None, None, None]
        elif t.ndim == 2:
            t = t[:, :, None, None]

        time_embedding = self.time_project(t)
        y = self.in_project(x)
        for block in self.convs:
            y = block(y, time_embedding, residual=True)
        return self.out_project(y)


