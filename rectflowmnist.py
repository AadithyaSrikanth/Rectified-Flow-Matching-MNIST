# Block 5: Training batch samplers

def sample_cfm_training_batch(x1: torch.Tensor):
    x0 = torch.randn_like(x1)
    t = torch.rand(x1.shape[0], 1, 1, 1, device=x1.device, dtype=x1.dtype)
    x_t = ImageFlow.interpolate(x0, x1, t)
    target = ImageFlow.target_velocity(x0, x1)
    return x_t, t, target


@torch.no_grad()
def sample_reflow_training_batch(
    teacher: nn.Module,
    batch_size: int,
    device: torch.device,
    pair_steps: int,
    pair_solver: str,
):
    x0 = torch.randn(batch_size, 1, 28, 28, device=device)
    x1 = integrate_flow(teacher, x0, num_steps=pair_steps, solver=pair_solver, return_path=False)
    t = torch.rand(batch_size, 1, 1, 1, device=device)
    x_t = ImageFlow.interpolate(x0, x1, t)
    target = ImageFlow.target_velocity(x0, x1)
    return x_t, t, target

"""**Block 5A — Metric Helpers**

This cell defines the three evaluation metrics used later in the notebook.

- The first is the **straightness measure**.
- The second is the **endpoint-consistency** measure
- The third is the **quadratic transport cost** \(\mathbb{E}\|Z_1-Z_0\|^2\), included as a special case of the paper’s convex transport-cost perspective.

To reduce Monte Carlo noise, the endpoint-consistency metric is averaged across multiple independent trajectory batches, and the checkpoint curves later use a small moving-average smoother for visualization.
"""

# Block 5A: Metric helpers (defined early so training can track checkpoints)

@torch.no_grad()
def estimate_paper_straightness(
    model: nn.Module,
    device: torch.device,
    batch_size: int = 128,
    pair_steps: int = 32,
    pair_solver: str = "midpoint",
    batches: int = 8,
):
    model.eval()
    vals = []
    for _ in range(batches):
        x0 = torch.randn(batch_size, 1, 28, 28, device=device)
        x1 = integrate_flow(model, x0, num_steps=pair_steps, solver=pair_solver, return_path=False)
        t = torch.rand(batch_size, 1, 1, 1, device=device)
        x_t = ImageFlow.interpolate(x0, x1, t)
        chord = x1 - x0
        pred = model(x_t, t)
        vals.append(F.mse_loss(pred, chord).item())
    return float(np.mean(vals))


@torch.no_grad()
def estimate_endpoint_consistency(
    model: nn.Module,
    device: torch.device,
    batch_size: int = 64,
    num_steps: int = 16,
    solver: str = "euler",
    batches: int = 4,
):
    model.eval()
    batch_vals = []
    for _ in range(batches):
        x0 = torch.randn(batch_size, 1, 28, 28, device=device)
        path = integrate_flow(model, x0, num_steps=num_steps, solver=solver, return_path=True)
        z1 = path[-1]
        errs = []

        for i in range(num_steps):
            t_scalar = i / num_steps
            zt = path[i]
            t = torch.full((batch_size, 1, 1, 1), t_scalar, device=device)
            vt = model(zt, t)
            z1_hat = zt + (1.0 - t_scalar) * vt
            errs.append(F.mse_loss(z1_hat, z1).item())

        batch_vals.append(float(np.mean(errs)))
    return float(np.mean(batch_vals))


@torch.no_grad()
def estimate_quadratic_transport_cost(
    model: nn.Module,
    device: torch.device,
    batch_size: int = 128,
    pair_steps: int = 32,
    pair_solver: str = "midpoint",
    batches: int = 8,
):
    model.eval()
    vals = []
    for _ in range(batches):
        x0 = torch.randn(batch_size, 1, 28, 28, device=device)
        x1 = integrate_flow(model, x0, num_steps=pair_steps, solver=pair_solver, return_path=False)
        disp = (x1 - x0).flatten(1)
        vals.append((disp.pow(2).sum(dim=1).mean()).item())
    return float(np.mean(vals))


@torch.no_grad()
def evaluate_metric_bundle(
    model: nn.Module,
    device: torch.device,
    straightness_batch_size: int = 128,
    straightness_pair_steps: int = 32,
    straightness_pair_solver: str = "midpoint",
    straightness_batches: int = 4,
    endpoint_batch_size: int = 128,
    endpoint_steps: int = 16,
    endpoint_solver: str = "euler",
    endpoint_batches: int = 2,
    transport_batch_size: int = 128,
    transport_pair_steps: int = 32,
    transport_pair_solver: str = "midpoint",
    transport_batches: int = 4,
):
    return {
        "paper_straightness": estimate_paper_straightness(
            model,
            device=device,
            batch_size=straightness_batch_size,
            pair_steps=straightness_pair_steps,
            pair_solver=straightness_pair_solver,
            batches=straightness_batches,
        ),
        "endpoint_consistency": estimate_endpoint_consistency(
            model,
            device=device,
            batch_size=endpoint_batch_size,
            num_steps=endpoint_steps,
            solver=endpoint_solver,
            batches=endpoint_batches,
        ),
        "quadratic_transport_cost": estimate_quadratic_transport_cost(
            model,
            device=device,
            batch_size=transport_batch_size,
            pair_steps=transport_pair_steps,
            pair_solver=transport_pair_solver,
            batches=transport_batches,
        ),
    }

