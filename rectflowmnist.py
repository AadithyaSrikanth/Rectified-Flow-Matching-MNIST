# Block 6: Training functions

def maybe_tqdm(iterable, enabled: bool = True, **kwargs):
    return tqdm(iterable, **kwargs) if enabled else iterable


def train_cfm_stage(
    model: nn.Module,
    loader: DataLoader,
    device: torch.device,
    epochs: int,
    lr: float,
    stage_name: str = "stage1_cfm",
    track_metrics: bool = True,
    track_metric_every: int = 1,
    track_straightness_batches: int = 6,
    track_endpoint_batches: int = 4,
    track_endpoint_steps: int = 16,
    checkpoint_dir: Path | None = None,
):
    optimizer = AdamW(model.parameters(), lr=lr, betas=(0.9, 0.99))
    losses = []
    history = []
    model.train()

    if checkpoint_dir is not None:
        checkpoint_dir.mkdir(parents=True, exist_ok=True)

    for epoch in range(epochs):
        running = 0.0
        count = 0
        batch_iter = maybe_tqdm(
            loader,
            enabled=SHOW_TQDM,
            desc=f"{stage_name} | epoch {epoch + 1}/{epochs}",
            leave=False,
            dynamic_ncols=True,
        )
        for batch_idx, (x1, _) in enumerate(batch_iter, start=1):
            x1 = x1.to(device)
            x_t, t, target = sample_cfm_training_batch(x1)

            optimizer.zero_grad(set_to_none=True)
            pred = model(x_t, t)
            loss = F.mse_loss(pred, target)
            loss.backward()
            optimizer.step()

            losses.append(loss.item())
            running += loss.item()
            count += 1

            if SHOW_TQDM and hasattr(batch_iter, "set_postfix") and (
                batch_idx % TQDM_UPDATE_EVERY == 0 or batch_idx == len(loader)
            ):
                batch_iter.set_postfix(batch_loss=f"{loss.item():.4f}", avg_loss=f"{running / count:.4f}")

        avg = running / max(1, count)
        entry = {"epoch": epoch + 1, "avg_loss": float(avg)}

        should_track = track_metrics and (((epoch + 1) % track_metric_every == 0) or (epoch + 1 == epochs))
        if should_track:
            metrics = evaluate_metric_bundle(
                model,
                device=device,
                straightness_batch_size=EVAL_BATCH_SIZE,
                straightness_pair_steps=PAIR_STEPS,
                straightness_pair_solver=PAIR_SOLVER,
                straightness_batches=track_straightness_batches,
                endpoint_batch_size=EVAL_BATCH_SIZE,
                endpoint_steps=track_endpoint_steps,
                endpoint_solver=SAMPLE_SOLVER,
                endpoint_batches=track_endpoint_batches,
                transport_batch_size=EVAL_BATCH_SIZE,
                transport_pair_steps=PAIR_STEPS,
                transport_pair_solver=PAIR_SOLVER,
                transport_batches=max(1, track_straightness_batches),
            )
            entry.update(metrics)
            print(
                f"[{stage_name}] epoch {epoch + 1}/{epochs} - loss: {avg:.6f} | "
                f"straightness: {metrics['paper_straightness']:.6f} | "
                f"endpoint_consistency: {metrics['endpoint_consistency']:.6f} | "
                f"quadratic_transport_cost: {metrics['quadratic_transport_cost']:.6f}"
            )
        else:
            print(f"[{stage_name}] epoch {epoch + 1}/{epochs} - loss: {avg:.6f}")

        history.append(entry)

        if checkpoint_dir is not None:
            torch.save(
                {
                    "model": model.state_dict(),
                    "losses": losses,
                    "history": history,
                    "epoch": epoch + 1,
                    "stage_name": stage_name,
                },
                checkpoint_dir / f"{stage_name}_epoch_{epoch + 1:02d}.pt",
            )

    return model, losses, history


