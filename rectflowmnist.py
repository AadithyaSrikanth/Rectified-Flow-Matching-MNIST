"""**Block 6A — One-Step Distillation Helpers**

These helpers implement the paper’s **final one-step distillation idea**.

After we obtain a \(k\)-rectified flow, we can train a direct one-step predictor
\[
\hat T(z_0)=z_0+v_	heta(z_0,0)
\]
to map the starting noise \(z_0\) directly to the final endpoint \(Z_1^k\).

For this section, the right metric is **one-step endpoint MSE**, because the distilled model no longer defines a full trajectory.  
The **straightness metric still belongs to the source rectified flow**, and it explains why this one-step approximation becomes easier after rectification.

"""

# Block 6A: One-step distillation helpers

@torch.no_grad()
def one_step_predict(model: nn.Module, x0: torch.Tensor) -> torch.Tensor:
    model.eval()
    t0 = torch.zeros(x0.shape[0], 1, 1, 1, device=x0.device, dtype=x0.dtype)
    return x0 + model(x0, t0)


@torch.no_grad()
def sample_images_one_step(
    model: nn.Module,
    n_samples: int,
    batch_size: int,
    device: torch.device,
):
    outs = []
    remaining = n_samples
    while remaining > 0:
        cur = min(batch_size, remaining)
        x0 = torch.randn(cur, 1, 28, 28, device=device)
        x1_hat = one_step_predict(model, x0)
        outs.append(x1_hat.cpu())
        remaining -= cur
    return torch.cat(outs, dim=0)


@torch.no_grad()
def estimate_t0_endpoint_consistency(
    flow_model: nn.Module,
    device: torch.device,
    batch_size: int = 128,
    pair_steps: int = 32,
    pair_solver: str = "midpoint",
    batches: int = 8,
):
    flow_model.eval()
    vals = []
    for _ in range(batches):
        x0 = torch.randn(batch_size, 1, 28, 28, device=device)
        z1 = integrate_flow(flow_model, x0, num_steps=pair_steps, solver=pair_solver, return_path=False)
        z1_hat = one_step_predict(flow_model, x0)
        vals.append(F.mse_loss(z1_hat, z1).item())
    return float(np.mean(vals))


@torch.no_grad()
def estimate_distilled_one_step_mse(
    distilled_model: nn.Module,
    source_flow: nn.Module,
    device: torch.device,
    batch_size: int = 128,
    pair_steps: int = 32,
    pair_solver: str = "midpoint",
    batches: int = 8,
):
    distilled_model.eval()
    source_flow.eval()
    vals = []
    for _ in range(batches):
        x0 = torch.randn(batch_size, 1, 28, 28, device=device)
        z1 = integrate_flow(source_flow, x0, num_steps=pair_steps, solver=pair_solver, return_path=False)
        z1_hat = one_step_predict(distilled_model, x0)
        vals.append(F.mse_loss(z1_hat, z1).item())
    return float(np.mean(vals))


def train_distillation_stage(
    source_flow: nn.Module,
    loader: DataLoader,
    device: torch.device,
    epochs: int,
    lr: float,
    stage_name: str,
    pair_steps: int,
    pair_solver: str,
    track_metrics: bool = True,
    track_metric_every: int = 1,
    checkpoint_dir: Path | None = None,
):
    student = copy.deepcopy(source_flow).to(device)
    source_flow.eval()
    student.train()

    optimizer = AdamW(student.parameters(), lr=lr, betas=(0.9, 0.99))
    losses = []
    history = []

    if checkpoint_dir is not None:
        checkpoint_dir.mkdir(parents=True, exist_ok=True)

    batch_size = loader.batch_size or 128
    for epoch in range(epochs):
        running = 0.0
        count = 0
        batch_iter = maybe_tqdm(
            loader,
            enabled=SHOW_TQDM,
            desc=f"{stage_name} | epoch {epoch + 1}/{epochs}",
            leave=False,
            dynamic_ncols=True,
        )
        for batch_idx, (_x, _) in enumerate(batch_iter, start=1):
            x0 = torch.randn(batch_size, 1, 28, 28, device=device)
            with torch.no_grad():
                z1 = integrate_flow(source_flow, x0, num_steps=pair_steps, solver=pair_solver, return_path=False)

            t0 = torch.zeros(batch_size, 1, 1, 1, device=device)
            target_disp = z1 - x0

            optimizer.zero_grad(set_to_none=True)
            pred_disp = student(x0, t0)
            loss = F.mse_loss(pred_disp, target_disp)
            loss.backward()
            optimizer.step()

            losses.append(loss.item())
            running += loss.item()
            count += 1

            if SHOW_TQDM and hasattr(batch_iter, "set_postfix") and (
                batch_idx % TQDM_UPDATE_EVERY == 0 or batch_idx == len(loader)
            ):
                batch_iter.set_postfix(batch_loss=f"{loss.item():.4f}", avg_loss=f"{running / count:.4f}")

        avg = running / max(1, count)
        entry = {"epoch": epoch + 1, "avg_loss": float(avg)}

        should_track = track_metrics and (((epoch + 1) % track_metric_every == 0) or (epoch + 1 == epochs))
        if should_track:
            one_step_mse = estimate_distilled_one_step_mse(
                distilled_model=student,
                source_flow=source_flow,
                device=device,
                batch_size=EVAL_BATCH_SIZE,
                pair_steps=PAIR_STEPS,
                pair_solver=PAIR_SOLVER,
                batches=max(2, TRACK_STRAIGHTNESS_BATCHES),
            )
            entry["one_step_endpoint_mse"] = one_step_mse
            print(
                f"[{stage_name}] epoch {epoch + 1}/{epochs} - loss: {avg:.6f} | "
                f"one_step_endpoint_mse: {one_step_mse:.6f}"
            )
        else:
            print(f"[{stage_name}] epoch {epoch + 1}/{epochs} - loss: {avg:.6f}")

        history.append(entry)

        if checkpoint_dir is not None:
            torch.save(
                {
                    "model": student.state_dict(),
                    "losses": losses,
                    "history": history,
                    "epoch": epoch + 1,
                    "stage_name": stage_name,
                },
                checkpoint_dir / f"{stage_name}_epoch_{epoch + 1:02d}.pt",
            )

    return student, losses, history

